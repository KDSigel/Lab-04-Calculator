export function sum(addFirstNum, addSecondNum) {
    return addFirstNum + addSecondNum;
}

export function difference(subFirstNum, subSecondNum) {
    return subFirstNum - subSecondNum;
}

export function product(multFirstNum, multSecondNum) {
    return multFirstNum * multSecondNum;
}

export function quotent(divFirstNum, divSecondNum) {
    return divFirstNum / divSecondNum;
}